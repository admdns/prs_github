function mouseOver(x)
{
switch (x)
{
    case -1:
        document.mm.src ="./image/menu0b.gif";
        break;
    case 0:
        document.m0.src ="./image/menu1b.gif";
        break;
    case 1:
        document.m1.src ="./image/menu2b.gif";
        break;
    case 2:
        document.m2.src ="./image/menu3b.gif";
        break;
    case 3:
        document.m3.src ="./image/menu4b.gif";
        break;
    case 4:
        document.m4.src ="./image/menu5b.gif";
        break;
    
}
}
function mouseOut(x)
{
switch (x)
{
    case -1:
        document.mm.src ="./image/menu0a.gif";
        break;
    case 0:
        document.m0.src ="./image/menu1a.gif";
        break;
    case 1:
        document.m1.src ="./image/menu2a.gif";
        break;
    case 2:
        document.m2.src ="./image/menu3a.gif";
        break;
    case 3:
        document.m3.src ="./image/menu4a.gif";
        break;
    case 4:
        document.m4.src ="./image/menu5a.gif";
        break;    
}
}